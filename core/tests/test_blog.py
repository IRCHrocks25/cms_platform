def _make_template():
    return Template.objects.create(name="Home tpl", html_source=HOME_HTML)


def _post(tenant, title, *, status=BlogPost.STATUS_PUBLISHED, featured=False, order=0, **kw):
    return BlogPost.objects.create(
        tenant=tenant,
        title=title,
        status=status,
        publish_date=timezone.now() if status == BlogPost.STATUS_PUBLISHED else None,
        featured=featured,
        featured_order=order,
        **kw,
    )


