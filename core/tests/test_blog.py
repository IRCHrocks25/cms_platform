    # --- end-to-end: the reported bug, through the real view flow ----------
    def test_strip_e2e_publish_feature_unfeature_unpublish_order(self):
        c = self._tenant()
        strip = reverse("dashboard:blog_strip_preview_self")

        # 1) Publish a post through the real form (no explicit publish_date —
        #    this is the path that previously left publish_date NULL).
        c.post(reverse("dashboard:blog_create_self"),
               {"title": "Alpha", "body": "<p>a</p>", "status": "published"})
        post = BlogPost.objects.get(tenant=self.tenant, title="Alpha")
        self.assertIsNotNone(post.publish_date)  # invariant holds

        # ...and feature it via the star toggle.
        c.post(reverse("dashboard:blog_featured_toggle_self", args=[post.pk]))
        r = c.get(strip)
        self.assertContains(r, "Alpha")            # (1) appears in the strip
        self.assertNotContains(r, "No published featured posts")

        # 2) Untoggle → disappears.
        c.post(reverse("dashboard:blog_featured_toggle_self", args=[post.pk]))
        self.assertNotContains(c.get(strip), "Alpha")

        # Re-feature for the unpublish check.
        c.post(reverse("dashboard:blog_featured_toggle_self", args=[post.pk]))
        self.assertContains(c.get(strip), "Alpha")

        # 3) Unpublish (back to draft) → disappears from the strip even though
        #    still featured (featured draft must NOT show publicly).
        c.post(reverse("dashboard:blog_edit_self", args=[post.pk]),
               {"title": "Alpha", "slug": post.slug, "body": "<p>a</p>",
                "status": "draft", "featured": "on"})
        post.refresh_from_db()
        self.assertTrue(post.featured)
        self.assertEqual(post.status, BlogPost.STATUS_DRAFT)
        self.assertNotContains(c.get(strip), "Alpha")

        # 4) Zero featured published posts → empty state shows.
        self.assertContains(c.get(strip), "No published featured posts")

        # 5) Ordering respects the manual featured_order.
        a = _post(self.tenant, "Order-A", featured=True, order=1)
        b = _post(self.tenant, "Order-B", featured=True, order=0)
        body = c.get(strip).content.decode()
        self.assertLess(body.index("Order-B"), body.index("Order-A"))  # order=0 first
