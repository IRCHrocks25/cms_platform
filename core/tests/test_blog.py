@override_settings(TENANT_BASE_DOMAIN="localhost")
class BlogPublicTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        User = get_user_model()
        cls.staff = User.objects.create_user("agency", password="x", is_staff=True)
        cls.member = User.objects.create_user("alice", password="x")
        tpl = _make_template()
        cls.tenant = Tenant.objects.create(
            name="Acme", subdomain="acme", template=tpl, owner=cls.staff, is_published=True,
        )
        TenantMembership.objects.create(tenant=cls.tenant, user=cls.member)
        cls.other = Tenant.objects.create(
            name="Beta", subdomain="beta", template=tpl, owner=cls.staff, is_published=True,
        )
        cls.pub = _post(cls.tenant, "Live Post", body="<p>hi</p><script>x</script>")
        cls.draft = _post(cls.tenant, "Secret Draft", status=BlogPost.STATUS_DRAFT)
        _post(cls.other, "Beta Only")

    def _anon(self):
        return Client(HTTP_HOST="acme.localhost")

    def test_index_lists_published(self):
        r = self._anon().get("/blog/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "Live Post")
        self.assertNotContains(r, "Secret Draft")

    def test_index_excludes_other_tenant(self):
        r = self._anon().get("/blog/")
        self.assertNotContains(r, "Beta Only")

    def test_detail_published_ok_and_sanitized(self):
        r = self._anon().get(f"/blog/{self.pub.slug}/")
        self.assertEqual(r.status_code, 200)
        self.assertNotIn(b"<script>x", r.content)

    def test_detail_draft_404_for_anon(self):
        r = self._anon().get(f"/blog/{self.draft.slug}/")
        self.assertEqual(r.status_code, 404)

    def test_detail_draft_visible_to_member(self):
        c = Client(HTTP_HOST="acme.localhost")
        c.force_login(self.member)
        r = c.get(f"/blog/{self.draft.slug}/")
        self.assertEqual(r.status_code, 200)

    def test_detail_injects_seo_title(self):
        self.pub.seo_title = "Custom SEO"
        self.pub.save()
        r = self._anon().get(f"/blog/{self.pub.slug}/")
        self.assertContains(r, "<title>Custom SEO</title>")

    def test_unpublished_site_blog_404(self):
        self.tenant.is_published = False
        self.tenant.save(update_fields=["is_published"])
        r = self._anon().get("/blog/")
        self.assertEqual(r.status_code, 404)

    def test_fallback_path_route(self):
        r = Client(HTTP_HOST="localhost").get(f"/site/acme/blog/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "Live Post")

    def test_detail_renders_when_footer_nested_in_content_section(self):
        # Regression: a plain <footer> (no data-section) nested INSIDE a content
        # section used to be destroyed when that section was decomposed, leaving
        # it detached so footer.insert_before() raised "Element has no parent"
        # (production 500 on /blog/.../ and the editor preview). Now the chrome
        # is rescued before decomposition.
        nested_footer_html = (
            "<!doctype html><html><head><title>Home</title></head><body>"
            "<h1 data-edit='hero.title' data-type='text'>Hi</h1>"
            "<section data-section='contact'><p>Reach us</p>"
            "<footer><p>nested foot</p></footer></section>"
            "</body></html>"
        )
        tpl = Template.objects.create(name="Nested footer", html_source=nested_footer_html)
        tenant = Tenant.objects.create(
            name="Nest", subdomain="nest", template=tpl, owner=self.staff, is_published=True,
        )
        post = _post(tenant, "Nested Footer Post", body="<p>body</p>")
        r = Client(HTTP_HOST="nest.localhost").get(f"/blog/{post.slug}/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "nested foot")  # footer survived, not dropped


