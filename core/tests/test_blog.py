class SanitizerTests(TestCase):
    def test_strips_script(self):
        self.assertNotIn("<script", sanitize_html("<p>ok</p><script>alert(1)</script>"))

    def test_strips_event_handlers(self):
        out = sanitize_html('<img src="https://x/a.png" onerror="alert(1)">')
        self.assertNotIn("onerror", out)

    def test_drops_javascript_href(self):
        out = sanitize_html('<a href="javascript:alert(1)">x</a>')
        self.assertNotIn("javascript", out)
        self.assertIn(">x</a>", out)

    def test_external_link_gets_rel(self):
        out = sanitize_html('<a href="https://example.com">e</a>')
        self.assertIn('rel="noopener noreferrer"', out)
        self.assertIn('target="_blank"', out)

    def test_keeps_allowed_tags(self):
        out = sanitize_html("<h2>H</h2><blockquote>q</blockquote><ul><li>a</li></ul>")
        self.assertIn("<h2>", out)
        self.assertIn("<blockquote>", out)
        self.assertIn("<li>", out)

    def test_unwraps_unknown_tag_keeping_text(self):
        out = sanitize_html('<div onclick="x()">keep</div>')
        self.assertNotIn("<div", out)
        self.assertIn("keep", out)

    def test_drops_style_and_class(self):
        out = sanitize_html('<p style="x" class="y">t</p>')
        self.assertNotIn("style=", out)
        self.assertNotIn("class=", out)


