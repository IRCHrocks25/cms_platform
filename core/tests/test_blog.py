@override_settings(TENANT_BASE_DOMAIN="localhost")
class BlogModelTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        User = get_user_model()
        cls.staff = User.objects.create_user("agency", password="x", is_staff=True)
        tpl = _make_template()
        cls.t1 = Tenant.objects.create(name="Acme", subdomain="acme", template=tpl, owner=cls.staff)
        cls.t2 = Tenant.objects.create(name="Beta", subdomain="beta", template=tpl, owner=cls.staff)

    def test_slug_autogenerated(self):
        p = _post(self.t1, "Hello World")
        self.assertEqual(p.slug, "hello-world")

    def test_slug_unique_within_tenant(self):
        _post(self.t1, "Hello World")
        p2 = _post(self.t1, "Hello World")
        self.assertEqual(p2.slug, "hello-world-2")

    def test_same_slug_across_tenants_ok(self):
        a = _post(self.t1, "Shared")
        b = _post(self.t2, "Shared")
        self.assertEqual(a.slug, b.slug)

    def test_display_excerpt_from_body(self):
        p = _post(self.t1, "T", body="<p>Hello there friend</p>", excerpt="")
        self.assertEqual(p.display_excerpt(), "Hello there friend")

    def test_resolved_seo_layers_over_site(self):
        p = _post(self.t1, "Title", seo_title="", seo_description="Desc", cover_image="https://c/x.png")
        seo = p.resolved_seo({"ga_measurement_id": "G-1", "page_title": "site"})
        self.assertEqual(seo["page_title"], "Title")  # falls back to title
        self.assertEqual(seo["meta_description"], "Desc")
        self.assertEqual(seo["og_image_url"], "https://c/x.png")
        self.assertEqual(seo["ga_measurement_id"], "G-1")  # site value preserved

    # --- publish_date invariant (regression: undated published posts were
    #     invisible to the strip / public queries) ---------------------------
    def test_published_post_gets_publish_date_on_save(self):
        # Mirrors admin / seed / non-form creation: no explicit publish_date.
        p = BlogPost.objects.create(
            tenant=self.t1, title="No Date", status=BlogPost.STATUS_PUBLISHED,
        )
        self.assertIsNotNone(p.publish_date)  # stamped by save()

    def test_explicit_publish_date_preserved(self):
        future = timezone.now() + timezone.timedelta(days=7)
        p = BlogPost.objects.create(
            tenant=self.t1, title="Scheduled",
            status=BlogPost.STATUS_PUBLISHED, publish_date=future,
        )
        self.assertEqual(p.publish_date, future)  # scheduled date not overwritten

    def test_draft_does_not_get_publish_date(self):
        p = BlogPost.objects.create(
            tenant=self.t1, title="Draft", status=BlogPost.STATUS_DRAFT,
        )
        self.assertIsNone(p.publish_date)

    def test_featured_published_without_explicit_date_appears_in_strip(self):
        from core.services import blog_render
        BlogPost.objects.create(
            tenant=self.t1, title="Ghost Buster",
            status=BlogPost.STATUS_PUBLISHED, featured=True,
        )
        names = [p.title for p in blog_render.featured_posts(self.t1, 6)]
        self.assertIn("Ghost Buster", names)

    def test_published_future_dated_post_is_visible(self):
        # The actual production bug: a Published+Featured post whose
        # publish_date sits in the future (the timezone-naive datetime-local
        # input stores a client's local "now" as a few hours ahead in UTC) was
        # excluded by the old `publish_date <= now` gate. Status governs
        # visibility now, so it must appear.
        from core.services import blog_render
        future = timezone.now() + timezone.timedelta(hours=6)
        BlogPost.objects.create(
            tenant=self.t1, title="Posted A Bit Early",
            status=BlogPost.STATUS_PUBLISHED, featured=True, publish_date=future,
        )
        names = [p.title for p in blog_render.featured_posts(self.t1, 6)]
        self.assertIn("Posted A Bit Early", names)
        # ...and a draft with a past date is still hidden (status gate intact).
        BlogPost.objects.create(
            tenant=self.t1, title="Still Draft", status=BlogPost.STATUS_DRAFT,
            featured=True, publish_date=timezone.now() - timezone.timedelta(days=1),
        )
        names = [p.title for p in blog_render.featured_posts(self.t1, 6)]
        self.assertNotIn("Still Draft", names)


