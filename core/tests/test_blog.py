@override_settings(TENANT_BASE_DOMAIN="localhost")
class BlogChromeTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        User = get_user_model()
        cls.staff = User.objects.create_user("agency", password="x", is_staff=True)
        cls.acme_tpl = Template.objects.create(name="Acme tpl", html_source=CHROME_HTML.replace("BRAND", "ACME"))
        cls.beta_tpl = Template.objects.create(name="Beta tpl", html_source=CHROME_HTML.replace("BRAND", "BETA"))
        cls.acme = Tenant.objects.create(
            name="Acme", subdomain="acme", template=cls.acme_tpl, owner=cls.staff, is_published=True,
        )
        cls.beta = Tenant.objects.create(
            name="Beta", subdomain="beta", template=cls.beta_tpl, owner=cls.staff, is_published=True,
        )
        cls.post = _post(cls.acme, "Wrapped Post", body="<p>hello world</p>")
        _post(cls.beta, "Beta Post")

    def _get(self, host, path):
        return Client(HTTP_HOST=host).get(path)

    def test_index_wrapped_in_site_chrome(self):
        r = self._get("acme.localhost", "/blog/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "ACME NAV")        # site navbar present
        self.assertContains(r, "ACME FOOTER")     # site footer present
        self.assertContains(r, "site-token")      # site global CSS present
        self.assertContains(r, "Wrapped Post")    # blog content present
        self.assertNotContains(r, "HERO HEADLINE")  # homepage section removed

    def test_detail_wrapped_in_site_chrome(self):
        r = self._get("acme.localhost", f"/blog/{self.post.slug}/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "ACME NAV")
        self.assertContains(r, "ACME FOOTER")
        self.assertContains(r, "cms-blog")          # scoped blog wrapper
        self.assertContains(r, "hello world")       # post body
        self.assertNotContains(r, "HERO HEADLINE")

    def test_each_client_gets_its_own_chrome(self):
        r = self._get("beta.localhost", "/blog/")
        self.assertEqual(r.status_code, 200)
        self.assertContains(r, "BETA NAV")
        self.assertNotContains(r, "ACME NAV")

    def test_detail_seo_title_in_wrapped_head(self):
        self.post.seo_title = "Wrapped SEO"
        self.post.save()
        r = self._get("acme.localhost", f"/blog/{self.post.slug}/")
        self.assertContains(r, "<title>Wrapped SEO</title>")

    def test_single_document_not_nested_html(self):
        # The wrapped output must be ONE document, not a blog doc nested in
        # the site doc (which would mean the fragment refactor regressed).
        r = self._get("acme.localhost", f"/blog/{self.post.slug}/")
        self.assertEqual(r.content.decode().lower().count("<body"), 1)

    def test_no_leaked_template_comment_syntax(self):
        # Regression: a multi-line {# #} comment leaked into the page as text.
        # Rendered blog HTML must never contain Django comment-hash syntax.
        for path in ("/blog/", f"/blog/{self.post.slug}/"):
            body = self._get("acme.localhost", path).content.decode()
            self.assertNotIn("{#", body, path)
            self.assertNotIn("#}", body, path)
            self.assertNotIn("CONTENT fragment", body, path)  # the comment text

    def test_chrome_offset_script_present(self):
        # The fixed/absolute-navbar offset logic ships (inlined) on blog pages.
        for path in ("/blog/", f"/blog/{self.post.slug}/"):
            r = self._get("acme.localhost", path)
            self.assertContains(r, "nav.offsetHeight")  # marker from the offset script

    def test_blog_stylesheet_survives_wrapping(self):
        # Regression: lxml hoisted the fragment's leading <style> into its
        # <head>, so wrap_in_site_chrome dropped the entire blog stylesheet and
        # the content rendered unstyled. The scoped CSS must be in the output.
        for path in ("/blog/", f"/blog/{self.post.slug}/"):
            body = self._get("acme.localhost", path).content.decode()
            self.assertIn(".cms-blog", body)
            self.assertIn(".blog-prose", body)      # typography rules present
            self.assertIn("blog-wrap", body)        # container rule present

    def test_container_detected_and_applied(self):
        # The blog wrapper should carry the site's detected container width +
        # gutter so it lines up with the navbar (test template uses 1000/40).
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        self.assertIn("--site-cw: 1000px", body)
        self.assertIn("--site-gut: 40px", body)

    def test_blog_css_is_fully_scoped(self):
        # Regression for the navbar-fade leak: every selector the blog emits
        # must be scoped under .cms-blog (or :root / @-rules), so nothing can
        # restyle the client's navbar/footer/chrome.
        from bs4 import BeautifulSoup
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        soup = BeautifulSoup(body, "lxml")
        leaked = []
        for st in soup.find_all("style"):
            css = st.string or ""
            if ".cms-blog" not in css:
                continue  # client's own stylesheet — leave it alone
            for rule in css.split("}"):
                if "{" not in rule:
                    continue
                sel = rule.split("{")[0].strip()
                if not sel or sel.startswith("@") or sel.startswith("/*"):
                    continue
                for part in sel.split(","):
                    p = part.strip()
                    if p and ".cms-blog" not in p and ":root" not in p:
                        leaked.append(p)
        self.assertEqual(leaked, [], "blog CSS leaked unscoped selectors: %s" % leaked)

    def test_navbar_masthead_backdrop_applied(self):
        # The client's hero background is reproduced behind the (transparent,
        # light-text) navbar so it stays readable on blog pages.
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        self.assertIn("--cms-masthead: #112233", body)
        self.assertIn(".cms-blog::before", body)  # the backdrop band

    # --- chrome interactivity: anchor links + scripts ----------------------
    def test_chrome_anchor_links_rewritten_on_blog(self):
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        self.assertIn('href="/#about"', body)       # nav anchor → homepage
        self.assertIn('href="/#programs"', body)    # footer anchor too

    def test_chrome_non_anchor_and_bare_hash_untouched(self):
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        self.assertIn('href="#"', body)             # bare # (logo / JS) kept
        self.assertNotIn('href="/#"', body)         # ...not rewritten
        self.assertIn('href="/contact"', body)      # absolute path kept
        self.assertIn('href="mailto:hi@x.com"', body)
        self.assertIn('href="https://ext.example"', body)

    def test_homepage_anchors_not_rewritten(self):
        body = self._get("acme.localhost", "/").content.decode()
        self.assertIn('href="#about"', body)        # homepage keeps in-page anchors
        self.assertNotIn('href="/#about"', body)

    def test_agency_fallback_anchor_prefix(self):
        body = self._get("localhost", f"/site/acme/blog/{self.post.slug}/").content.decode()
        self.assertIn('href="/site/acme/#about"', body)

    def test_chrome_scripts_present_on_blog(self):
        body = self._get("acme.localhost", f"/blog/{self.post.slug}/").content.decode()
        self.assertIn("window.__chrome_ready", body)         # body-level site script
        self.assertIn("window.__hero_section_script", body)  # rescued from removed section


